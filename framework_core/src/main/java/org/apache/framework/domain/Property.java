package org.apache.framework.domain;

public class Property {

	private String name;
	
	private String showTextName;
	
	private String remarks; //备注

	private String codeGroupNo;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getShowTextName() {
		return showTextName;
	}

	public void setShowTextName(String showTextName) {
		this.showTextName = showTextName;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getCodeGroupNo() {
		return codeGroupNo;
	}

	public void setCodeGroupNo(String codeGroupNo) {
		this.codeGroupNo = codeGroupNo;
	}

}
